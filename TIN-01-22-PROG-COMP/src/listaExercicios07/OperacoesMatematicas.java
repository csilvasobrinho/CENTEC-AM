package listaExercicios07;

public class OperacoesMatematicas {
	//Método para somar.
	double somar(double valor1, double valor2) {
		double soma = valor1 + valor2;
		return soma;
	}
	//Método para subtrair.
	double subtrair (double valor1, double valor2) {
		double diferenca = valor1 - valor2;
		return diferenca;
	}
	//Método para multiplicar.
	double multiplicar(double valor1, double valor2) {
		double produto = valor1 * valor2;
		return produto;
	}
	//Método para Dividir.
	double dividir(double valor1, double valor2) {
		double divisao = valor1 / valor2;
		return divisao;
	}

}
